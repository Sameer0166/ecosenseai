// Helper to determine pollution level/color
export const getAQILevel = (aqi: number): PollutionLevel => {
  if (aqi <= 50) return PollutionLevel.Good;
  if (aqi <= 100) return PollutionLevel.Moderate;
  if (aqi <= 150) return PollutionLevel.UnhealthySensitive;
  if (aqi <= 200) return PollutionLevel.Unhealthy;
  return PollutionLevel.Hazardous;
};

export const getLevelColor = (level: PollutionLevel): string => {
  switch (level) {
    case PollutionLevel.Good: return 'text-emerald-400';
    case PollutionLevel.Moderate: return 'text-yellow-400';
    case PollutionLevel.UnhealthySensitive: return 'text-orange-400';
    case PollutionLevel.Unhealthy: return 'text-red-500';
    case PollutionLevel.Hazardous: return 'text-purple-500';
    default: return 'text-gray-400';
  }
};

export const getBgColor = (level: PollutionLevel): string => {
  switch (level) {
    case PollutionLevel.Good: return 'bg-emerald-500/20 border-emerald-500/30';
    case PollutionLevel.Moderate: return 'bg-yellow-500/20 border-yellow-500/30';
    case PollutionLevel.UnhealthySensitive: return 'bg-orange-500/20 border-orange-500/30';
    case PollutionLevel.Unhealthy: return 'bg-red-500/20 border-red-500/30';
    case PollutionLevel.Hazardous: return 'bg-purple-500/20 border-purple-500/30';
    default: return 'bg-gray-500/20';
  }
};

